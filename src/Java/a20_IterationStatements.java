package Java;

public class a20_IterationStatements {
	/*
	 * 반복문
	 * 프로그램 내에서 똑같은 명령을 일정 횟수만큼 반복하여 수행하도록 제어하는 명령문
	 * 	while문
	 * 	특정 조건을 만족할 때까지 계속해서 주어진 명령문을 반복 실행
	 * 	
	 * 	do/while문
	 * 	루프를 한 번 실행한 후에 조건식을 검사
	 * 	문법
	 * 		do{
	 * 			조건식의 결과가 참인 동안 반복적으로 실행하고자 하는 명령문;
	 * 		}while(조건식);
	 * 
	 * 	for문
	 * 	자체적으로 초기식, 조건식, 증감식을 모두 포함하고 있는 반복문
	 * 
	 * 	Enhanced for문
	 * 	컬렉션 프레임워크와 배열에서 유용하게 자주 사용됨
	 */
}
