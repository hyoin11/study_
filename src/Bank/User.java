package Bank;

public class User {
	private int userIdx;
	private String userId;
	private String userPwd;
	private String userName;
	private String userBirthday;
	private String userPhone;
	private int userAccountIdx;
	private int userAccount;
	private int userCash;
	
	public int getUserIdx() {
		return userIdx;
	}
	
	public void setUserIdx(int userIdx) {
		this.userIdx = userIdx;
	}
	
	public String getUserId() {
		return userId;
	}
	
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	public String getUserPwd() {
		return userPwd;
	}
	
	public void setUserPwd(String userPwd) {
		this.userPwd = userPwd;
	}
	
	public String getUserName() {
		return userName;
	}
	
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	public String getUserBirthday() {
		return userBirthday;
	}
	
	public void setUserBirthday(String userBirthday) {
		this.userBirthday = userBirthday;
	}
	
	public String getUserPhone() {
		return userPhone;
	}
	
	public void setUserPhone(String userPhone) {
		this.userPhone = userPhone;
	}
	
	public int getUserAccountIdx() {
		return userAccountIdx;
	}
	
	public void setUserAccountIdx(int userAccountIdx) {
		this.userAccountIdx = userAccountIdx;
	}
	
	public int getUserAccount() {
		return userAccount;
	}
	
	public void setUserAccount(int userAccount) {
		this.userAccount = userAccount;
	}
	
	public int getUserCash() {
		return userCash;
	}
	
	public void setUserCash(int userCash) {
		this.userCash = userCash;
	}
}
